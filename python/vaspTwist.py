def read_POSCAR(filepath):

    if not os.path.exists(filepath):
        print(f"ERROR!\nFile: {filepath} does not exist.")
        exit(1)

    with open(filepath, 'r') as f:
        lines = f.readlines()

    if len(lines[1].split()) == 1:
        raw_scale = float(lines[1])
        raw_lat   = np.array([[float(x) for x in line.split()]
                               for line in lines[2:5]])
        if raw_scale < 0:
            volume = abs(np.linalg.det(raw_lat))
            scale  = np.cbrt(abs(raw_scale) / volume)
        elif raw_scale == 0:
            print("ERROR! Scaling factor must not be zero.")
            exit(1)
        else:
            scale = raw_scale
        lattice = raw_lat * scale
    elif len(lines[1].split()) == 3:
        scale   = np.array(list(map(float, lines[1].split())))
        lattice = np.array([[float(x) * scale[i] for i, x in enumerate(line.split())]
                             for line in lines[2:5]])
    else:
        print("ERROR! Scaling factor must be 1 or 3 components.")
        exit(1)

    elements  = []
    is_number = lines[5].split()[0].isdecimal()
    if is_number:
        for i in range(len(lines[5].split())):
            while True:
                name = input(f"Enter the name of species No. {i+1:>3}: ").strip()
                if name.isalpha():
                    break
                print("Species name must be alphabetic only.")
            elements.append(name)
        atom_counts = [int(x) for x in lines[5].split()]
        sel_dyn     = lines[6].lower().startswith('s')
        pos_start   = 8 if sel_dyn else 7
    else:
        for name in lines[5].split():
            elements.append(name.split('/')[0].split('_')[0])
        atom_counts = [int(x) for x in lines[6].split()]
        sel_dyn     = lines[7].lower().startswith('s')
        pos_start   = 9 if sel_dyn else 8

    total    = sum(atom_counts)
    pos_stop = pos_start + total
    positions = np.array([[float(x) for x in lines[i].split()[:3]]
                           for i in range(pos_start, pos_stop)])
    species  = [el for el, cnt in zip(elements, atom_counts)
                for _ in range(cnt)]

    flags = None
    if sel_dyn:
        flags = np.array([[x for x in lines[i].split()[3:6]]
                           for i in range(pos_start, pos_stop)])

    is_direct = lines[pos_start - 1].strip().lower().startswith('d')
    if is_direct:
        pos_direct = positions % 1.0
        pos_cart   = direct_to_cart(lattice, pos_direct)
    else:
        pos_cart   = positions * scale
        pos_direct = cart_to_direct(lattice, pos_cart)

    return {"lattice":    lattice,
            "elements":   elements,
            "counts":     atom_counts,
            "total":      total,
            "pos_cart":   pos_cart,
            "pos_direct": pos_direct,
            "species":    species,
            "sel_dyn":    sel_dyn,
            "flags":      flags}


def write_POSCAR(filepath, lattice, elements, counts, pos_direct,
                 sel_dyn, flags, labels):

    with open(filepath, 'w') as o:
        o.write("Generated by vaspTwist.py\n")
        o.write(f"   {1.0:.14f}\n")
        for row in lattice:
            o.write(f"   {row[0]:20.16f}  {row[1]:20.16f}  {row[2]:20.16f}\n")
        o.write("   " + "    ".join(elements) + " \n")
        o.write("     " + "    ".join(map(str, counts)) + "\n")
        if sel_dyn:
            o.write("Selective dynamics\n")
        o.write("Direct\n")
        if sel_dyn:
            for p, fl, lb in zip(pos_direct, flags, labels):
                o.write(f"{p[0]:20.16f}{p[1]:20.16f}{p[2]:20.16f}"
                        f"   {fl[0]}   {fl[1]}   {fl[2]}   {lb:>6s}\n")
        else:
            for p, lb in zip(pos_direct, labels):
                o.write(f"{p[0]:20.16f}{p[1]:20.16f}{p[2]:20.16f}"
                        f"   {lb:>6s}\n")

# ---------------------------------------------------------------------------
# Coordinate transforms
# ---------------------------------------------------------------------------

def direct_to_cart(lattice, pos_direct):
    return np.dot(pos_direct % 1.0, lattice)

def cart_to_direct(lattice, pos_cart):
    return np.dot(pos_cart, np.linalg.inv(lattice)) % 1.0

# ---------------------------------------------------------------------------
# Rotation matrix around z-axis (Rodrigues' formula with u = [0, 0, 1])
#
# Rodrigues' rotation formula for unit axis u and angle theta:
#   R = cos(theta)*I + sin(theta)*(u x) + (1-cos(theta))*(u⊗u)
#
# For u = z = [0, 0, 1]:
#   u x I  = [[0,-1,0],[1,0,0],[0,0,0]]   (cross-product matrix)
#   u⊗u    = [[0,0,0],[0,0,0],[0,0,1]]
#
# This simplifies to the standard 2D rotation matrix with z unchanged:
#   R = [[ cos, -sin, 0 ],
#        [ sin,  cos, 0 ],
#        [  0,    0,  1 ]]
# ---------------------------------------------------------------------------

def rotation_matrix(angle_deg):
    """
    3x3 rotation matrix around the z-axis by angle_deg degrees.
    Derived from Rodrigues' formula with u = [0, 0, 1].
    """
    u      = np.array([0., 0., 1.])
    theta  = np.radians(angle_deg)
    sin    = np.sin(theta)
    cos    = np.cos(theta)
    rotate = (cos * np.eye(3)
              + sin * np.cross(np.eye(3), u)
              + (1 - cos) * np.outer(u, u))
    return rotate

# ---------------------------------------------------------------------------
# Expansion matrix validation and replicas extraction
# ---------------------------------------------------------------------------

def make_expansion_matrix(M2):
    """
    Convert a 2x2 in-plane supercell matrix M2 into a 3x3 expansion matrix
    and extract the number of replicas (= determinant).

    The z-direction is left as 1 (vacuum slab — never replicated in z).

    Applies the same validity checks as input_expansion():
      - determinant must be positive
      - determinant must be an integer (within 1e-6 tolerance)
    """
    expansion_matrix = np.eye(3, dtype=float)
    expansion_matrix[:2, :2] = M2.astype(float)

    det     = np.linalg.det(expansion_matrix)
    det_int = int(round(det))

    if det <= 1e-10:
        raise ValueError(
            f"Invalid expansion matrix: determinant = {det:.6f} "
            f"(must be a positive integer).")
    if abs(det - det_int) >= 1e-6 or det_int <= 0:
        raise ValueError(
            f"Invalid expansion matrix: determinant = {det:.6f} "
            f"is not a positive integer.")

    return expansion_matrix, det_int

# ---------------------------------------------------------------------------
# Build supercell (provided function — unchanged)
# ---------------------------------------------------------------------------

def build_supercell(expansion_matrix,
                    replicas,
                    lattice_matrix,
                    atom_counts,
                    total_atoms,
                    positions_cartesian,
                    species,
                    selective_dynamics,
                    flags):

    new_lattice_matrix = np.dot(expansion_matrix, lattice_matrix)

    corners = np.array([[i, j, k]
                        for i in range(2) for j in range(2) for k in range(2)])
    corner_transformed = np.dot(corners, expansion_matrix)
    min_points = np.min(corner_transformed, axis=0).astype(int)
    max_points = np.max(corner_transformed, axis=0).astype(int) + 1
    all_points = np.array([[i, j, k] for i in range(min_points[0], max_points[0])
                                     for j in range(min_points[1], max_points[1])
                                     for k in range(min_points[2], max_points[2])])
    adj = np.round(np.linalg.inv(expansion_matrix) * replicas).astype(int)
    frac_points = np.dot(all_points, adj)
    mask = (np.all(frac_points >= 0, axis=1) &
            np.all(frac_points <  replicas, axis=1))
    grid_points = all_points[mask]

    if len(grid_points) != replicas:
        print(f"WARNING: Expected {replicas} grid points but found {len(grid_points)}. "
              "Check your expansion matrix.")

    grid_cartesian = np.dot(grid_points, lattice_matrix)
    new_positions_cartesian = (
        positions_cartesian[:, np.newaxis, :] +
        grid_cartesian[np.newaxis, :, :]
    ).reshape(-1, 3)
    new_species = np.repeat(species, len(grid_points)).tolist()

    new_flags = None
    if selective_dynamics:
        new_flags = np.tile(flags[:, np.newaxis, :],
                            (1, len(grid_points), 1)).reshape(-1, 3)

    new_atom_counts = [count * replicas for count in atom_counts]
    new_total_atoms = total_atoms * replicas

    new_positions_direct = cart_to_direct(new_lattice_matrix, new_positions_cartesian)

    return {"lattice_matrix":      new_lattice_matrix,
            "atom_counts":         new_atom_counts,
            "total_atoms":         new_total_atoms,
            "positions_direct":    new_positions_direct,
            "positions_cartesian": new_positions_cartesian,
            "species":             new_species,
            "flags":               new_flags}

