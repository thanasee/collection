def print_table(solutions):
    header = (f"  {'#':>4}  {'Angle (deg)':>12}  {'m':>4}  {'n':>4}  "
              f"{'N':>5}  {'Tot.atoms':>10}  "
              f"{'Strain(%)':>10}  "
              f"{'dL1(%)':>7}  {'dL2(%)':>7}  {'dAng(%)':>8}  "
              f"{'Size(x)':>8}")
    print(header)
    print("  " + "-" * (len(header) - 2))
    for i, sol in enumerate(solutions):
        print(f"  {i+1:>4}  {sol['angle_deg']:>12.5f}  "
              f"{sol['m']:>4}  {sol['n']:>4}  "
              f"{sol['N']:>5}  {sol['total_atoms']:>10}  "
              f"{sol['strain_pct']:>10.4f}  "
              f"{sol['dL1_pct']:>7.4f}  {sol['dL2_pct']:>7.4f}  "
              f"{sol['dAng_pct']:>8.4f}  "
              f"{sol['size_ratio']:>8.3f}")
    print()
    print("  Strain(%) = max(dL1, dL2, dAng) -- overall mismatch between layers")
    print("  dL1 / dL2 = relative length mismatch of supercell vectors 1 and 2")
    print("  dAng      = |angle_bot - angle_top| / 180 * 100")
    print()


def prompt_selection(solutions):
    while True:
        raw = input(
            "  Enter number(s) to build (e.g.  2  or  1 3 5  or  all): "
        ).strip().lower()

        if raw == "all":
            return list(solutions)
        if raw in ("", "none", "q", "quit"):
            return []

        tokens = raw.replace(",", " ").split()
        try:
            indices = [int(t) for t in tokens]
        except ValueError:
            print("  ERROR! Enter integer number(s), 'all', or press Enter to quit.")
            continue

        bad = [i for i in indices if i < 1 or i > len(solutions)]
        if bad:
            print(f"  ERROR! Out of range: {bad}. Valid: 1 – {len(solutions)}.")
            continue

        seen, chosen = set(), []
        for i in indices:
            if i not in seen:
                seen.add(i)
                chosen.append(solutions[i - 1])
        return chosen

