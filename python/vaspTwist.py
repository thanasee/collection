def main():

    args = argv[1:]
    if not args or '-h' in args or '--help' in args:
        usage()

    poscar_file = args[0]
    working_dir = os.getcwd()
    mono        = read_POSCAR(poscar_file)
    n_prim      = mono["total"]

    print(f"\n{'='*72}")
    print(f"  vaspTwist.py -- Commensurate twist-angle search")
    print(f"  Input file      : {poscar_file}")
    print(f"  Primitive atoms : {n_prim}")
    print(f"  Atom limit      : {MAX_ATOMS}  (both layers combined)")
    print(f"  Table filter    : strain <= {STRAIN_SHOW:.1f}%")
    print(f"{'='*72}\n")

    all_solutions = enumerate_twist_angles(mono["lattice"], n_prim)

    if not all_solutions:
        print(f"  No commensurate angles found (atom limit = {MAX_ATOMS}).")
        return

    shown  = [s for s in all_solutions if s["strain_pct"] <= STRAIN_SHOW]
    hidden = len(all_solutions) - len(shown)

    print(f"  Found {len(all_solutions)} solution(s) total; "
          f"showing {len(shown)} with strain <= {STRAIN_SHOW:.1f}%"
          + (f"  ({hidden} hidden, edit STRAIN_SHOW to see more)" if hidden else "")
          + ":\n")

    if not shown:
        print(f"  No solutions pass the strain filter.")
        print(f"  Edit STRAIN_SHOW at the top of the script to see more.")
        return

    print_table(shown)
    chosen = prompt_selection(shown)

    if not chosen:
        print("\n  Nothing selected. Exiting.\n")
        return

    # Ask about z-flip once, applies to all chosen angles
    flip = False
    thickness = (np.max(mono["pos_cart"][:, 2]) -
                 np.min(mono["pos_cart"][:, 2]))
    if thickness > 1e-8:
        while True:
            ans = input("\n  Flip top layer in z (Y/N): ").strip().upper()
            if ans and ans[0] == 'Y':
                flip = True;  break
            if ans and ans[0] == 'N':
                break
            print("  ERROR! Enter Y or N.")

    print()
    written = 0
    for sol in chosen:
        tag  = f"twist_{sol['angle_deg']:.4f}deg_m{sol['m']}_n{sol['n']}"
        odir = os.path.join(working_dir, tag)
        os.makedirs(odir, exist_ok=True)
        try:
            data   = build_twist_poscar(mono, sol, flip=flip)
            labels = make_labels(data["elements"], data["counts"])
            write_POSCAR(os.path.join(odir, "POSCAR"),
                         data["lattice"],
                         data["elements"],
                         data["counts"],
                         data["pos_direct"],
                         data["sel_dyn"],
                         data["flags"],
                         labels)
            total = sum(data["counts"])
            print(f"  Written : {tag}/POSCAR  "
                  f"({total} atoms, strain = {sol['strain_pct']:.4f}%)")
            written += 1
        except Exception as e:
            print(f"  WARNING : could not build {tag} -- {e}")

    print(f"\n  Done -- {written} POSCAR(s) written.\n")


if __name__ == "__main__":
    main()
