def check_elements(elements):
    unique = list(dict.fromkeys(elements))
    if len(elements) == len(unique):
        return None
    print("\nFound duplicated elements in POSCAR!")
    print("Unique elements: [" + " ".join(unique) + "]")
    while True:
        order = input("Enter desired element order (space-separated): ").split()
        if not order:
            order = unique.copy()
        if len(order) == len(unique) and set(order) == set(unique):
            return order
        print("ERROR! Species do not match unique elements. Try again.")


def reorder_atoms(elements, counts, pos_cart, pos_direct,
                  species, sel_dyn, flags, sort_order):
    ec, ed, esp, ef = {}, {}, {}, {}
    idx = 0
    for el, cnt in zip(elements, counts):
        ec .setdefault(el, []).extend(pos_cart  [idx:idx+cnt].tolist())
        ed .setdefault(el, []).extend(pos_direct[idx:idx+cnt].tolist())
        esp.setdefault(el, []).extend(species   [idx:idx+cnt])
        if sel_dyn:
            ef.setdefault(el, []).extend(flags  [idx:idx+cnt].tolist())
        idx += cnt

    nc, nd, ns, nf, new_counts = [], [], [], [], []
    for el in sort_order:
        nc.extend(ec [el]);  nd.extend(ed [el])
        ns.extend(esp[el]);  new_counts.append(len(ed[el]))
        if sel_dyn:
            nf.extend(ef[el])

    return {"elements":   sort_order,
            "counts":     new_counts,
            "pos_cart":   np.array(nc, dtype=float),
            "pos_direct": np.array(nd, dtype=float),
            "species":    ns,
            "flags":      np.array(nf) if sel_dyn else None}


def make_labels(elements, counts):
    digits = len(str(max(counts))) + 1
    return [f"{el}{str(i).zfill(digits)}"
            for el, cnt in zip(elements, counts)
            for i in range(1, cnt + 1)]

# ---------------------------------------------------------------------------
# Sheet centering (z at fractional 0.5)
# ---------------------------------------------------------------------------

def unwrap(pos_direct):
    ref   = pos_direct[0].copy()
    delta = pos_direct - ref
    delta -= np.round(delta)
    return ref, ref + delta

def center_sheet(pos_direct):
    ref, unwrapped = unwrap(pos_direct)
    center = np.mean(unwrapped, axis=0)
    new    = unwrapped.copy()
    new[:, :2] = unwrapped[:, :2] - ref[:2]
    new[:,  2] = unwrapped[:,  2] - center[2] + 0.5
    return new % 1.0

# ---------------------------------------------------------------------------
# GCD
# ---------------------------------------------------------------------------

def gcd(a, b):
    a, b = abs(int(round(a))), abs(int(round(b)))
    while b:
        a, b = b, a % b
    return a

# ---------------------------------------------------------------------------
# Partner-vector search
#
# Given t1 = m*a1 + n*a2, we need a partner t2 = p*a1 + q*a2 such that
# (t1, t2) form a valid 2D supercell.  t2 must:
#   1. Be a real lattice vector (integer p, q combination of a1, a2)
#   2. Not be collinear with t1  (would give zero-area cell)
#   3. Have length as close to |t1| as possible  (minimise cell distortion)
#
# Search radius derivation
# ------------------------
# We need to find t2 whose length is close to |t1|.
# The maximum length of t1 is:
#     |t1| <= |m|*|a1| + |n|*|a2| <= (|m|+|n|) * max(|a1|, |a2|)
#
# A lattice vector p*a1 + q*a2 of length L requires indices of at most:
#     max(|p|, |q|) <= L / min_proj
# where min_proj is the smallest projection of any basis vector onto any
# direction, i.e. the shortest distance between parallel lattice lines:
#     min_proj = area / max(|a1|, |a2|)   (= perpendicular height of cell)
#
# Combining: to reach length |t1|, we need at most
#     max(|p|, |q|) <= |t1| / min_proj
#                    = |t1| * max(|a1|,|a2|) / area
#                   <= (|m|+|n|) * max(|a1|,|a2|)^2 / area
#
# We define this as the radius, with a small integer buffer (+2) to guard
# against floating-point rounding near the boundary.
# ---------------------------------------------------------------------------

def partner_search_radius(m, n, lat2d):
    """
    Compute the minimum safe integer search radius for find_partner.

    The radius guarantees that no lattice vector of length <= |t1| is missed.
    Derived from the lattice geometry rather than a fixed heuristic.

    Parameters
    ----------
    m, n  : int   — supercell indices defining t1 = m*a1 + n*a2
    lat2d : (2,2) — in-plane lattice matrix, rows = a1, a2

    Returns
    -------
    radius : int
    """
    a1, a2   = lat2d[0], lat2d[1]
    len_a1   = np.linalg.norm(a1)
    len_a2   = np.linalg.norm(a2)
    len_max  = max(len_a1, len_a2)
    area     = abs(np.cross(a1, a2))

    # Upper bound on |t1|
    len_t1_max = (abs(m) + abs(n)) * len_max

    # Minimum perpendicular height of the primitive cell
    # (shortest distance between parallel rows of lattice points)
    min_proj = area / len_max   # = min(|a1|*sin, |a2|*sin) of cell angle

    # Maximum index needed to reach a vector of length len_t1_max
    # Guard against degenerate (zero-area) cells
    if min_proj < 1e-10 or len_max < 1e-10:
        return abs(m) + abs(n) + 2

    radius = int(np.ceil(len_t1_max / min_proj)) + 2

    return radius


def find_partner(m, n, lat2d):
    """
    For t1 = m*a1 + n*a2, find the lattice vector t2 = p*a1 + q*a2 that:
      - is NOT collinear with t1  (non-zero cell area)
      - has length closest to |t1|  (minimise supercell distortion)

    The search radius is derived from the lattice geometry via
    partner_search_radius(), guaranteeing no candidate is missed.

    Returns (t1, t2, N) where N = |area(t1,t2)| / area_prim  (replicas).
    """
    a1, a2 = lat2d[0], lat2d[1]
    t1     = m * a1 + n * a2
    lt1    = np.linalg.norm(t1)
    area_p = abs(np.cross(a1, a2))
    radius = partner_search_radius(m, n, lat2d)

    best_diff = np.inf
    t2_best   = None
    N_best    = 0

    for p in range(-radius, radius + 1):
        for q in range(-radius, radius + 1):
            if p == 0 and q == 0:
                continue
            cand  = p * a1 + q * a2
            lc    = np.linalg.norm(cand)
            if lc < 1e-10:
                continue
            # Reject collinear vectors (cross product ≈ 0)
            sin_a = abs(np.cross(t1, cand)) / (lt1 * lc)
            if sin_a < 1e-6:
                continue
            diff = abs(lc - lt1) / lt1
            if diff < best_diff:
                best_diff = diff
                t2_best   = cand
                N_best    = max(1, round(abs(np.cross(t1, cand)) / area_p))

    return t1, t2_best, N_best

# ---------------------------------------------------------------------------
# Strain between bottom and top supercells
# ---------------------------------------------------------------------------

def vec_angle_deg(v1, v2):
    cos = np.dot(v1, v2) / (np.linalg.norm(v1) * np.linalg.norm(v2) + 1e-30)
    return np.degrees(np.arccos(np.clip(cos, -1, 1)))


def calc_strain(t1_bot, t2_bot, t1_top, t2_top):
    """Returns (strain_pct, dL1_pct, dL2_pct, dAng_pct)."""
    l1b, l2b = np.linalg.norm(t1_bot), np.linalg.norm(t2_bot)
    l1t, l2t = np.linalg.norm(t1_top), np.linalg.norm(t2_top)

    if min(l1b, l2b, l1t, l2t) < 1e-10:
        return 100., 100., 100., 100.

    dL1  = abs(l1b - l1t) / max(l1b, l1t) * 100.
    dL2  = abs(l2b - l2t) / max(l2b, l2t) * 100.
    dAng = abs(vec_angle_deg(t1_bot, t2_bot) -
               vec_angle_deg(t1_top, t2_top)) / 180. * 100.

    return max(dL1, dL2, dAng), dL1, dL2, dAng

# ---------------------------------------------------------------------------
# Automatic enumeration of commensurate angles
# ---------------------------------------------------------------------------

def enumerate_twist_angles(lattice, n_prim):
    """
    Find all commensurate twist angles whose bilayer fits within MAX_ATOMS.
    Returns list of solution dicts sorted by angle.
    """
    lat2d     = lattice[:2, :2].copy()
    a1, a2    = lat2d[0], lat2d[1]
    area_prim = abs(np.cross(a1, a2))

    solutions   = []
    seen_angles = {}

    for m in range(1, MAX_SC + 1):
        if 2 * m * n_prim > MAX_ATOMS:
            break

        for n in range(0, m + 1):
            if gcd(m, n) != 1:
                continue

            t1_bot, t2_bot, N = find_partner(m, n, lat2d)
            if t2_bot is None or N == 0:
                continue
            if np.linalg.norm(t2_bot) < 1e-10:
                continue

            total_atoms = 2 * N * n_prim
            if total_atoms > MAX_ATOMS:
                continue

            angle = vec_angle_deg(t1_bot, t2_bot)
            if angle < 1e-4 or angle > 179.9999:
                continue

            R         = rotation_matrix(angle)
            lat2d_top = (R[:2, :2] @ lat2d.T).T

            t1_top, t2_top, _ = find_partner(m, n, lat2d_top)
            if t2_top is None:
                continue

            strain, dL1, dL2, dAng = calc_strain(
                t1_bot, t2_bot, t1_top, t2_top)

            sc_a       = 0.5 * (t1_bot + t1_top)
            sc_b       = 0.5 * (t2_bot + t2_top)
            area_sc    = abs(np.cross(sc_a, sc_b))
            size_ratio = area_sc / area_prim

            sc_lat = np.zeros((3, 3))
            sc_lat[0, :2] = sc_a
            sc_lat[1, :2] = sc_b
            sc_lat[2]     = lattice[2]

            inv_bot = np.linalg.inv(lat2d)
            inv_top = np.linalg.inv(lat2d_top)
            M_bot   = np.array([np.round(t1_bot @ inv_bot).astype(int),
                                 np.round(t2_bot @ inv_bot).astype(int)])
            M_top   = np.array([np.round(t1_top @ inv_top).astype(int),
                                 np.round(t2_top @ inv_top).astype(int)])

            if np.linalg.det(M_bot) < 0.5 or np.linalg.det(M_top) < 0.5:
                continue

            angle_key = round(angle, 3)
            entry = {"angle_deg":   angle,
                     "m":           m,
                     "n":           n,
                     "N":           N,
                     "M_bot":       M_bot,
                     "M_top":       M_top,
                     "sc_lat":      sc_lat,
                     "strain_pct":  strain,
                     "dL1_pct":     dL1,
                     "dL2_pct":     dL2,
                     "dAng_pct":    dAng,
                     "size_ratio":  size_ratio,
                     "total_atoms": total_atoms}

            if angle_key in seen_angles:
                idx = seen_angles[angle_key]
                if strain < solutions[idx]["strain_pct"]:
                    solutions[idx] = entry
            else:
                seen_angles[angle_key] = len(solutions)
                solutions.append(entry)

    solutions.sort(key=lambda x: x["angle_deg"])
    return solutions

# ---------------------------------------------------------------------------
# Build twisted bilayer POSCAR using build_supercell
# ---------------------------------------------------------------------------

def build_twist_poscar(mono, sol, flip=False):
    """
    Build the twisted bilayer supercell in AA stacking.

    Steps:
      1. Center the monolayer sheet at z = 0.5.
      2. Convert M_bot -> (expansion_matrix, replicas) via make_expansion_matrix.
      3. Expand bottom layer with build_supercell.
      4. Rotate primitive lattice and positions around z by twist angle.
      5. Convert M_top -> (expansion_matrix, replicas) via make_expansion_matrix.
      6. Expand top layer with build_supercell.
      7. Shift top layer above bottom by interlayer gap.
      8. Wrap both layers into the shared supercell (sc_lat).
      9. Merge and sort elements.
    """
    lattice = mono["lattice"]
    angle   = sol["angle_deg"]
    M_bot   = sol["M_bot"]
    M_top   = sol["M_top"]
    sc_lat  = sol["sc_lat"]

    R = rotation_matrix(angle)

    # ---- Centred primitive positions ----
    prim_dir  = center_sheet(mono["pos_direct"])
    prim_cart = direct_to_cart(lattice, prim_dir)

    # ---- Bottom layer ----
    E_bot, replicas_bot = make_expansion_matrix(M_bot)
    bot = build_supercell(
              expansion_matrix    = E_bot,
              replicas            = replicas_bot,
              lattice_matrix      = lattice,
              atom_counts         = mono["counts"],
              total_atoms         = mono["total"],
              positions_cartesian = prim_cart,
              species             = mono["species"],
              selective_dynamics  = mono["sel_dyn"],
              flags               = mono["flags"])

    # ---- Top layer: rotate lattice and positions around z, then expand ----
    lat_top       = R @ lattice               # rotate all lattice vectors
    top_prim_cart = (R @ prim_cart.T).T       # rotate all atomic positions

    E_top, replicas_top = make_expansion_matrix(M_top)
    top = build_supercell(
              expansion_matrix    = E_top,
              replicas            = replicas_top,
              lattice_matrix      = lat_top,
              atom_counts         = mono["counts"],
              total_atoms         = mono["total"],
              positions_cartesian = top_prim_cart,
              species             = mono["species"],
              selective_dynamics  = mono["sel_dyn"],
              flags               = mono["flags"])

    # ---- Shift top layer above bottom layer ----
    thickness = np.max(prim_cart[:, 2]) - np.min(prim_cart[:, 2])
    gap       = max(3.0, thickness + 3.0)
    top["positions_cartesian"][:, 2] += gap

    if flip:
        zmax = np.max(top["positions_cartesian"][:, 2])
        zmin = np.min(top["positions_cartesian"][:, 2])
        top["positions_cartesian"][:, 2] = zmax + zmin - top["positions_cartesian"][:, 2]

    # ---- Wrap into shared supercell ----
    inv_sc  = np.linalg.inv(sc_lat)
    bot_dir = (bot["positions_cartesian"] @ inv_sc) % 1.0
    top_dir = (top["positions_cartesian"] @ inv_sc) % 1.0
    all_dir = np.vstack([bot_dir, top_dir])
    all_cart = all_dir @ sc_lat

    # ---- Merge elements and counts ----
    elements = mono["elements"] + mono["elements"]
    counts   = bot["atom_counts"] + top["atom_counts"]
    species  = bot["species"] + top["species"]

    sort_order = check_elements(elements)
    if sort_order is not None:
        mapped   = reorder_atoms(elements, counts, all_cart, all_dir,
                                 species, False, None, sort_order)
        elements = mapped["elements"]
        counts   = mapped["counts"]
        all_dir  = mapped["pos_direct"]

    return {"lattice":    sc_lat,
            "elements":   elements,
            "counts":     counts,
            "pos_direct": all_dir,
            "sel_dyn":    False,
            "flags":      None}

